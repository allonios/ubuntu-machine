#!usr/bin/env bash



# enable minimize on click on Ubuntu dock
# ref: https://askubuntu.com/questions/960074/how-do-i-enable-minimize-on-click-on-ubuntu-dock-in-17-10-and-later
###############################################################################################################################################
gsettings set org.gnome.shell.extensions.dash-to-dock click-action 'minimize'
###############################################################################################################################################


# enable hibernate:
###############################################################################################################################################
sudo apt install pm-utils && sudo pm-hibernate -y
###############################################################################################################################################


# time sycn
###############################################################################################################################################
timedatectl set-local-rtc 1
###############################################################################################################################################
